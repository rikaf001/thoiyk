public class reportFormulaClass {
    


 static public java.lang.Integer SumSum(
    java.lang.Integer a, 
    java.lang.Integer b
  ) {  
      java.lang.Integer r = 0;

      for (java.lang.Integer i=a;i<=b;i++) {
        r+=i;
      }
      return r;
    }
}